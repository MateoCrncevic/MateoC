    private void init() {
        
        repo = RepositoryFactory.getRepository();
        
        fields.add(tfUsername);
        fields.add(tfPassword);
        
        errorLabels.add(lblUsernameError);
        errorLabels.add(lblPasswordError);
    }

    private boolean formValid() {
        
        Boolean ok = true;
        for(int i = 0; i < fields.size(); i++){
        
          if( fields.get(i).getText().isEmpty()){
          
          errorLabels.get(i).setText("X");
          ok = false;
          }
        
        }
        
        
        return ok;
    }
