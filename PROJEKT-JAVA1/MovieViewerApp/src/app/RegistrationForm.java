    private void btnRegisterActionPerformed(java.awt.event.ActionEvent evt) {//GEN-FIRST:event_btnRegisterActionPerformed
       
        errorLabels.forEach(label -> label.setText(""));
        if(formValid()){
        
            try {
                repo.registerLogin(tfUsername.getText().trim(), tfPassword.getText().trim());
                MessageUtils.showInformationMessage("Registration", "Account created!");
                LoginForm form = new LoginForm();
                form.setVisible(true);
                this.dispose();
                
            } catch (Exception ex) {
                MessageUtils.showErrorMessage("Registration", "An error has occurred");
            }
        }
    }//GEN-LAST:event_btnRegisterActionPerformed

